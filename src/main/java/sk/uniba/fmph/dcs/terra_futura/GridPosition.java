package sk.uniba.fmph.dcs.terra_futura;

public final class GridPosition {
    private final int x;
    private final int y;

    public GridPosition(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int x() {
        return x;
    }

    public int y() {
        return y;
    }


}
